package CommonEnums;

/**
 * Represents the status of the game.
 */
public enum Status {
        /**
         * The game is currently active.
         */
        ACTIVE,
        /**
         * The game is saved.
         */
        SAVED,
        /**
         * The black player has won.
         */
        BLACK_WIN,
        /**
         * The white player has won.
         */
        WHITE_WIN,
        /**
         * The game is a stalemate.
         */
        STALEMATE;
}
